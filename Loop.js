// Sum of n natural no
let na = 5;
let sum = 0;
for (let index = 0; index <= na; index++) {
    sum += index
    }
console.log(`Sum of ${na} natura no is ${sum}`);


// Factorial of n
let n = 4
let fact = 1;
for (let i = 1; i <= n; i++) {
    
    fact = fact * i;
}
console.log(`Factorial of ${n} Natural no is ${fact}`);
